function arrTime(){

var d = Date();

var ta = d.split(" ")[4].split(":").join("").split(""); // makes an array of single digit strings

var h = ta.shift();
h += ta.shift();

var hour = parseInt(h);

if (hour > 12){
	hour -= 12;
} else if (hour === 0){
	hour = 12;
}

return hour.toString().split("").concat(ta);



}

